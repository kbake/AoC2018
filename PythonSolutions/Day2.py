def figure_correct_ids(id_list):
  correct_ids = []
  for i in range(0, len(id_list)-1):
    cur_id = id_list[i]
    next_id = id_list[i+1]
    num_diff = 0
    common_letters = ""
    for x, y in zip(cur_id, next_id):
      if x == y:
        common_letters += x
      else:
        num_diff += 1
    if num_diff == 1:
      correct_ids.append((cur_id, next_id, common_letters))
  return correct_ids

words = puzzle_input.splitlines()
words.sort()
correct_ids = figure_correct_ids(words)
